package team.projectA.vo;

public class ReviewVO extends LodgingVO{ 
	
	private int rvidx;
	private int uidx;
	private int lidx;
	private String rvTitle;
	private String rvSatisfaction;
	private String rvContent;
	private String rvDate;
	
	
	public int getRvidx() {
		return rvidx;
	}
	public void setRvidx(int rvidx) {
		this.rvidx = rvidx;
	}
	public int getUidx() {
		return uidx;
	}
	public void setUidx(int uidx) {
		this.uidx = uidx;
	}
	public int getLidx() {
		return lidx;
	}
	public void setLidx(int lidx) {
		this.lidx = lidx;
	}
	public String getRvTitle() {
		return rvTitle;
	}
	public void setRvTitle(String rvTitle) {
		this.rvTitle = rvTitle;
	}
	public String getRvSatisfaction() {
		return rvSatisfaction;
	}
	public void setRvSatisfaction(String rvSatisfaction) {
		this.rvSatisfaction = rvSatisfaction;
	}
	public String getRvContent() {
		return rvContent;
	}
	public void setRvContent(String rvContent) {
		this.rvContent = rvContent;
	}
	public String getRvDate() {
		return rvDate;
	}
	public void setRvDate(String rvDate) {
		this.rvDate = rvDate;
	}
	
	
	

}
